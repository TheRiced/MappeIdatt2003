package ntnu.idatt2003.model;

/**
 * Represents the available difficulty levels for a board game.
 * <ul>
 *   <li>{@link #EASY} - A standard, easy game board.</li>
 *   <li>{@link #ADVANCED} - An advanced board with more complex features.</li>
 *   <li>{@link #CUSTOM} - A user-created custom board.</li>
 * </ul>
 */
public enum GameLevel {
  EASY,
  ADVANCED,
  CUSTOM
}
